package com.example.QuadTree;
import java.util.*;
public class Camp {

    private float x;
    private float y;

    public Camp(float x, float y) {
        this.x = x;
        this.y = y;
    }

    // getters & toString()


    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }


}